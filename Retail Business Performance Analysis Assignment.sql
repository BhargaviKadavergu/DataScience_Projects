-- 22. Find the second highest selling product.

select productid,productname,TotalSales 
from( select p.productid,productname,sum(od.quantity) as TotalSales , 
       row_number() over ( order by sum(od.quantity) desc ) as  rn
       from products as p
       inner join orderdetails as od on p.productid=od.productid
       group by p.ProductID,p.ProductName) as ranked
where rn=2;
       
-- 23. Identify duplicate orders placed on the same day.

select o.customerid,o.orderdate,count(o.orderid) as DuplicateCount
from orders as o
group by o.customerid,o.orderdate
having count(o.orderid)>1;



